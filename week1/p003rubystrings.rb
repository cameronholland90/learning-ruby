# Can use " or ' for Strings, but ' is more efficient
puts "Hello World"	# Both output: Hello World
puts 'Hello World'

# String concatenation
puts 'I like' + ' Ruby'		# Outputs: I like Ruby

# Escape sequence
puts 'It\'s my Ruby'	# Outputs: It's my Ruby

# New here, displays the string three times
puts 'Hello' * 3	# Outputs: HelloHelloHello

# Defining a constant
PI = 3.1416
puts PI 	# Outputs: 3.1416