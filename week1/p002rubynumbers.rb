# p002rubynumbers.rb
=begin
	Ruby Numers
	Usual operators
	+ addition
	- subtraction
	* multiplication
	/ division	
=end

puts 1 + 2 	# outputs 3
puts 2 * 3	# outputs 6

# Integer division
# When you do arithmetic with integers, you'll get integer answers
puts 3 / 2		# outputs 1
puts 10 - 11	# outputs -1
puts 1.5 / 2.6	# outputs 0.5769230769230769

# using modulo
puts (5 % 3)	# outputs 2
puts (-5 % 3)	# outputs 1
puts (5 % -3)	# outputs -1
puts (-5 % -3)	# outputs -2

# or and || operator
puts nil || 2008	# outputs 2008
puts false || 2008	# outputs 2008
puts "ruby" || 2008	# outputs ruby

